public class compareNumbers {

    public static void main(String[] args) {
        compareNumbers();
    }

    public static void compareNumbers() {
        int a = 10; // Инициализация переменной a значением 10
        int b = 5; // Инициализация переменной b значением 5

        if (a >= b) {
            System.out.println("a >= b"); // Вывод сообщения, если a больше или равно b
        } else {
            System.out.println("a < b"); // Вывод сообщения, если a меньше b
        }
    }


}